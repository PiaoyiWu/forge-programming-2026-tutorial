    @Override
    public void periodic() {
        doRejectUpdateOne = false;
        doRejectUpdateTwo = false;


        /*
         * Periodically try to apply the operator perspective.
         * If we haven't applied the operator perspective before, then we should apply it regardless of DS state.
         * This allows us to correct the perspective in case the robot code restarts mid-match.
         * Otherwise, only check and apply the operator perspective if the DS is disabled.
         * This ensures driving behavior doesn't change until an explicit disable event occurs during testing.
         */
        if (!m_hasAppliedOperatorPerspective || DriverStation.isDisabled()) {
            DriverStation.getAlliance().ifPresent(allianceColor -> {
                setOperatorPerspectiveForward(
                    allianceColor == Alliance.Red
                        ? kRedAlliancePerspectiveRotation
                        : kBlueAlliancePerspectiveRotation
                );
                m_hasAppliedOperatorPerspective = true;
            });
        }


        // VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT VISION MEASUREMENT 
        // ################################################################################################################################################################################################################


        

        if (!hasInitializedYaw) {
            this.seedHeadingWithVision();

            hasInitializedYaw = true;
        }

        LimelightHelpers.SetRobotOrientation("limelight-one", this.getPigeon2().getYaw().getValueAsDouble(), 0, 0, 0, 0, 0);
        LimelightHelpers.PoseEstimate limelightOne_mt1 = LimelightHelpers.getBotPoseEstimate_wpiBlue_MegaTag2("limelight-one");
   
        LimelightHelpers.SetFiducialIDFiltersOverride("limelight-one", validIDs);
        
        // if our angular velocity is greater than 360 degrees per second, ignore vision updates
        if(Math.abs(this.getPigeon2().getAngularVelocityZWorld().getValueAsDouble()) > 360) {
            doRejectUpdateOne = true;

        }

        

        if(limelightOne_mt1 == null || limelightOne_mt1.tagCount == 0) {
            doRejectUpdateOne = true;

        }

        LimelightHelpers.SetRobotOrientation("limelight-two", this.getPigeon2().getYaw().getValueAsDouble(), 0, 0, 0, 0, 0);
        LimelightHelpers.PoseEstimate limelightTwo_mt1 = LimelightHelpers.getBotPoseEstimate_wpiBlue_MegaTag2("limelight-two");

        LimelightHelpers.SetFiducialIDFiltersOverride("limelight-two", validIDs);


        // if our angular velocity is greater than 360 degrees per second, ignore vision updates
        if(Math.abs(this.getPigeon2().getAngularVelocityZWorld().getValueAsDouble()) > 360) {
            doRejectUpdateTwo = true;

        }

        if(limelightTwo_mt1 == null || limelightTwo_mt1.tagCount == 0) {
            doRejectUpdateTwo = true;

        }

        // maybe use rawFiducials[0].distToCamera > distanceThreshold

        if (limelightOne_mt1 != null &&limelightOne_mt1.avgTagArea < Constants.CommandSwerveDrivetrainConstants.areaThreshold) {
            doRejectUpdateOne = true;
        }

        if (limelightTwo_mt1 != null && limelightTwo_mt1.avgTagArea < Constants.CommandSwerveDrivetrainConstants.areaThreshold) {
            doRejectUpdateTwo = true;
        }

        if (!doRejectUpdateOne && !doRejectUpdateTwo) {
            if (limelightOne_mt1 != null && limelightTwo_mt1 != null && limelightOne_mt1.avgTagArea > limelightTwo_mt1.avgTagArea) {
                doRejectUpdateTwo = true;
            } else {
                doRejectUpdateOne = true;
            }
        }

        // also may not need to iterate through entire array if most of the calculations are taken by order or closest distance or least ambiguity. If that is the case, use rawFudicials[0] only to reject update

        if (limelightOne_mt1 != null && limelightOne_mt1.rawFiducials != null) {
            oneLLIDs = new double[limelightOne_mt1.rawFiducials.length];
            ambiguitiesOne = new double[limelightOne_mt1.rawFiducials.length];
            ambiguitiesOneAvg = 0;
            for (int i = 0; i < limelightOne_mt1.rawFiducials.length; i++) {
                ambiguitiesOne[i] = limelightOne_mt1.rawFiducials[i].ambiguity;
                oneLLIDs[i] = limelightOne_mt1.rawFiducials[i].id;

                // if (ambiguitiesOne[i] > Constants.CommandSwerveDrivetrainConstants.ambiguityThreshold) {
                //     doRejectUpdateOne = true;
                // }
                ambiguitiesOneAvg += ambiguitiesOne[i];
            }
            ambiguitiesOneAvg /= limelightOne_mt1.rawFiducials.length;
            if (ambiguitiesOneAvg > Constants.CommandSwerveDrivetrainConstants.ambiguityThreshold) {
                doRejectUpdateOne = true;
            }
        }

        if (limelightTwo_mt1 != null && limelightTwo_mt1.rawFiducials != null) {
            twoLLIDs = new double[limelightTwo_mt1.rawFiducials.length];
            ambiguitiesTwo = new double[limelightTwo_mt1.rawFiducials.length];
            ambiguitiesTwoAvg = 0;
            for (int i = 0; i < limelightTwo_mt1.rawFiducials.length; i++) {
                ambiguitiesTwo[i] = limelightTwo_mt1.rawFiducials[i].ambiguity;
                twoLLIDs[i] = limelightTwo_mt1.rawFiducials[i].id;

                // if (ambiguitiesTwo[i] > Constants.CommandSwerveDrivetrainConstants.ambiguityThreshold) {
                //     doRejectUpdateTwo = true;
                // }
                ambiguitiesTwoAvg += ambiguitiesTwo[i];
            }
            ambiguitiesTwoAvg /= limelightTwo_mt1.rawFiducials.length;

            if (ambiguitiesTwoAvg > Constants.CommandSwerveDrivetrainConstants.ambiguityThreshold) {
                doRejectUpdateTwo = true;
            }
        }

        if(!doRejectUpdateOne) {
            this.setVisionMeasurementStdDevs(VecBuilder.fill(Constants.CommandSwerveDrivetrainConstants.limelightStdDevs, Constants.CommandSwerveDrivetrainConstants.limelightStdDevs, Constants.CommandSwerveDrivetrainConstants.limelightStdDevs));
            this.addVisionMeasurement(
                limelightOne_mt1.pose,
                limelightOne_mt1.timestampSeconds);
            
            // seedHeadingWithVision(limelightOne_mt1);

        }

        if(!doRejectUpdateTwo) {
            this.setVisionMeasurementStdDevs(VecBuilder.fill(Constants.CommandSwerveDrivetrainConstants.limelightStdDevs, Constants.CommandSwerveDrivetrainConstants.limelightStdDevs, Constants.CommandSwerveDrivetrainConstants.limelightStdDevs));
            this.addVisionMeasurement(
                limelightTwo_mt1.pose,
                limelightTwo_mt1.timestampSeconds);

            // seedHeadingWithVision(limelightTwo_mt1);
        }
        checkPitchAndRoll();
        updateSmartDashboard();

    }

