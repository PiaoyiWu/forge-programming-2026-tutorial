        Translation2d turretCenterOfRotation = new Translation2d(Constants.TurretShooterConstants.turretXOffset * Constants.CommandSwerveDrivetrainConstants.rotatingWhileStrafingTestMultiplier, Constants.TurretShooterConstants.turretYOffset * Constants.CommandSwerveDrivetrainConstants.rotatingWhileStrafingTestMultiplier);
